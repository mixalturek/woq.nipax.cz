<h3>Co, jak a kde koupit</h3>

<p>V podstatě všechny obchody fungují 24 hodin denně. Na větší nákupy potravin se vyplatí zajít do řetězců Командор nebo Красный Яр, menší nákupy jsem dělal po cestě v Троя nebo Алпы. Ovoce bývá nejlepší/nejčerstvější/nejlevnější na rynku - teda pokud není zrovna mínus třicet ;-).</p>

<p>Velké obchodní centrum Planeta je na Взлётке, je tam jediný obchod typu Smíchovského Tesca, který jsem viděl v Krasnojarsku.</p>

<p>Normální boty se v podstatě nedají sehnat, naprostá většina nabídky je extrémně nekvalitní (umělá hmota na první pohled) a všechno úplně stejné. Pro slečny nápodobně, pouze s rozdílem deseticentimetrových podpatků a bez sebemenší špetky vkusu při návrhu. Na druhou stranu při kupování bot na treking zkuste zajít do značkových obchodů typu Adidas/Nike, narazil jsem na slevu ze čtyř tisíc rublů na dva, neuvěřitelná a nečekaná cena.</p>


