<h4>Něco na sebe</h4>

<p>Ač se to nezdá, protože jsou teploty mnohem nižší, v krizi se dá chodit i v našem běžném oblečení, prostě si vezmete dvě vrstvy. Obyčejná čepice plně dostačuje, klasické vlněné/pletené rukavice jsou asi nejlepší co se týká tepla a praktičnosti, nicméně doporučuji spíše palčáky než rukavice s oddělenými prsty.</p>

<p>Spodky jsou vyloženě nutností, ještě v Česku jsem si koupil Moira a naprostá spokojenost. Přežil jsem v nich i výlet na Stolby, šest, sedm hodin při mínus patnácti. Teplé kotníkové boty a tlusté, pletené ponožky se taky můžou hodit. Při jarním tání jsou třeba pevné nepromokavé boty! Nemají tu kanály, takže všechna voda zůstává na ulicích a trvá týden, dva, než se vypaří. Trekingové boty se osvědčily.</p>

<p>Teploty jsou asi takové <a href="http://www.24temp.ru/">http://www.24temp.ru/</a>, ale spíš o pár stupňů nižší, než je vidět na grafech. Jeden týden v únoru bylo mínus 35 (ve smyslu celý týden), ale to už byl extrém i pro Rusy. Při mínus dvaceti se dá bez problémů existovat a chodit na procházky, mínus deset je už naprosto bez problémů. Nebojte se toho, vnímání teplot je tu dost posunuté, zvyknete si během pár dní.</p>


<h2>Cesta tam a zpět</h2>

<h3>Letenka</h3>

<p>Letenku je možné hledat na <a href="http://www.dohop.com/">http://www.dohop.com/</a>. Ceny z Prahy se pohybují v řádu 15 000 Kč, a proto doporučuji zkusit i lety z okolních metropolí. Podstatnou část ceny tvoří let do Moskvy, který je z Prahy dost drahý. Letenka Vídeň - Moskva - Krasnoyarsk se dá najít i za 6000 až 7000 Kč v závislosti na kurzu měn. Autobusy Student Agency zastavují ve Vídni přímo u letiště.
</p>

<p>
Je vhodné si najít let bez složitého přesunu v Moskvě mezi letišti, který by stál mnohem více, než dělá rozdíl cen letenek. Já jsem letěl Vídeň - Moskva Domodědovo - Krasnoyarsk za 7500 Kč, vlak byl dokonce dražší. V Moskvě je dobré mít i v rámci jednoho letiště alespoň několik hodin na přestup, ve frontě na razítko do pasu jsme čekali dvě hodiny.
</p>

<p>
Co se týká cesty zpátky, zkuste <a href="http://startravel.ru/">http://startravel.ru/</a>, v Krasnojarsku mají kancelář na Prospektu Mira. Údajně poskytují studentům s ISIC kartou slevu ~30 procent.
</p>


<h3>Procedůra na hranicích - co mít u sebe</h3>

<p>Na hranicích je u sebe potřeba mít cestovní pas s platným vízem a vyplněný State border crossing report (dávají v letadle, nezapomenout!). Na letišti Domodědovo v Moskvě jsme čekali v gigantické frontě cca. dvě hodiny, potom už žádné problémy nebyly, pas po půl minutě vrátili zpět. Zavazadla jsme našli i po tak dlouhé době v pořádku.</p>


<h3>Nečekané změny</h3>

<p>Letiště Домодедово v Moskvě zadrželo letadlo, takže odlítalo o 45 minut později a ve Vídni jsem proto nestihl autobus do Brna. Moje druhá zkušenost s ním, nevěřte tomuhle letišti, při přestupech si nechávejte víc času.</p>


<h2>Po příjezdu</h2>

<h3>Pick up nebo samostatná cesta z letiště</h3>

<p>Byl jsem domluvený se známými, kteří před rokem studovali v Praze a kterým jsem dělal buddíka (ISC), že mě na letišti vyzvednou. Předpokládám, že jinak by mě vyzvedával někdo z mezinárodního oddělení univerzity jako ostatní Čechy. Letiště je dost daleko za městem a doprava taková, že bych rozhodně nechtěl podnikat tuhle cestu sám.</p>


