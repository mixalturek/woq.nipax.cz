<h2>Před návratem</h2>

<h3>Formality na univerzitě</h3>

<p>Poté, co máte všechny zápočty a zkoušky hotové, odevzáte svůj index v mezinárodním a požádáte o vyhotovení Transcript of records. Pokud bude dostatek času dostanete ho přímo do rukou, jinak ho budou odesílat poštou na ČVUT. Druhým papírem je potvrzení délky pobytu.</p>


<h3>Ostatní - jak poslat nebo kde nechat koupené věci, rozloučení</h3>

<p>Spoustu věcí jako nádobí apod. mám půjčené od ruských kámošů, takže budu vracet. Ostatní věci, které nepovezu domů, jim nejspíš rozdám taky, na kolejích bych je nenechával. Pošta do Česka prý funguje, cena se pohybuje okolo 1800 rublů za 15 kg, ale nakonec jsem nic neposílal.</p>


<h2>Po návratu</h2>

<h3>Formality na fakultě</h3>

<p>Je třeba vyplnit "Žádost o uznání zkoušek a zápočtů" a poté oběhat vedoucí kateder, na kterých se vyučují dané předměty. Poté, co máte všechny podpisy, vyplňte ještě formulář <a href="http://www.cvut.cz/informace-pro-studenty/studium-v-zahranici/erasmus/studium/po-navratu">Doklad o uznání předmětů</a> a zajděte s těmito papíry na Pedagogické oddělení fakulty.</p>

<p>Na katedře ekonomiky po mě v emailu chtěli, abych vzal s sebou také osnovu předmětu, který jsem studoval v zahraničí, ale nakonec se na ni ani nepodívali - naštěstí. Možná by bylo dobré tohle shánět ještě v Rusku, protože hledání Экономики природопользования na stránkách СибГАУ bylo naprosto neúspěšné. Nakonec jsem sice něco našel přes google na nějakém ruském studentském serveru, ale nejsem si jistý, jak moc by to bylo relevantní pro uznávání předmětu.</p>

<p>Dále si dejte pozor, co vám napíší do počtu hodin u absolvovaných předmětů. Jeden mi neuznali, a to i přesto, že jsem ho měl v předběžném plánu. Byl jsem domluvený, že udělám nějaký projekt a nebudu chodit na přednášky, protože jsem už většinu znal. Nakonec se to vymstilo.</p>


<h3>Formality na OZS Rektorát</h3>

<p>Po příjezdu pouze přinesete potvrzení o délce pobytu a Transcript of Records, žádné problémy.</p>


