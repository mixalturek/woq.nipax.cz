<h2>Před odjezdem</h2>

<h3>Přihlášky na zahraniční univerzitu</h3>

<p>Podávají se na zahraniční oddělení rektorátu do stanoveného termínu, viz <a href="http://www.cvut.cz/informace-pro-studenty/studium-v-zahranici/mbd/rusko">web rektorátu</a>.</p>


<h3>Žádost o vízum</h3>

<p>Všechny informace o vízách do Ruska jsou na adrese <a href="http://www.czech.mid.ru">http://www.czech.mid.ru/</a>, část "Konzulární oddělení". Hodně dlouho se čeká na pozvání z univerzity (přišlo cca. v polovině prosince), poté stačilo vyplnit formulář a zajít na konzulát, do deseti dnů byla hotová. Víza jsou pro studenty bezplatná. Pozor na ruské zimní svátky, začínají na konci prosince a končí tuším 14. února, dá se to stihnout i potom, ale proč se stresovat.</p>

<p>Vízum se dává vždy na tři měsíce a poté se musí prodloužit. V Krasnoyarsku se vším pomohli lidé ze zahraničního oddělení a nebyly s tím žádné problémy. Moje platilo od 26. ledna do 30. června, druhé bylo vícevstupé.</p>

<p>Pokud se jede vlakem jsou nutná navíc i tranzitní víza.</p>


<h3>Formality na ČVUT</h3>

<p>Je třeba včas podat přihlášku k zahraničnímu pobytu. Po oznámení ze strany rektorátu, že jedete, se vyplňuje další přihláška, která se posílá na СибГАУ, cca. pět stran - osobní údaje, učební zaměření atd.</p>

<p>Cestovní pas musí být platný nejméně 6 měsíců po návratu. Dále je třeba si na studijním fakulty vyžádat výpis studijních výsledků a donést ho na rektorát. Jeho vyhotovení trvá cca. dva týdny.</p>

<p>Po podepsání finanční dohody je nutné si vyřídit cestovní pojištění na celou dobu pobytu, viz pojištění níže.</p>

<p>Dále je třeba si vytvořit studijní plán na zahraniční univerzitě. U mě to probíhalo tak, že jsem napsal email Ирине Суровес ze zahraničního oddělení СибГАУ, se kterou jsem byl v kontaktu, kde se dají najít informace o předmětech. V té době na webu <a href="http://www.sibsau.ru/">http://www.sibsau.ru/</a> nebylo vůbec nic. Odeslala mi cca. desetistránkový dokument, ve kterém na každém řádku bylo jedno jméno předmětu, vše v ruštině.</p>

<p>Na základě toho jsem vyplnil formulář a nechal si ho potvrdit na Katedře počítačů FEL doc. Kubátovou. Podepsala se k předmětům ze všech kateder se slovy, že je to stejně byrokratická zbytečnost. Dále je nutné ještě podpis studijního prorektora (paní Demlová). Doporučuji si udělat několik kopií, protože na pedagogickém oddělení FEL se věci rády ztrácejí a poté se za několik měsíců, již nepotřebné, opět objevují.</p>

<p>Je možné mít zároveň zapsány i předměty v česku, typicky Individuální projekt před diplomovou prací, a po získání zápočtu dostat kredity. Na studijním je dále nutné si na následující semestr nechat zapsat předmět SOCRA - Socrates Erasmus, aby vás nevyřadili ze seznamu studentů. To se sice stejně stane (email z KOSu z emailové adresy uživatele Testovac Test6), protože odjíždíte před řádným zápisem a nebudete to mít oficiálně potvrzené, ale poté se dá v emailech argumentovat tím, že je to jejich chyba.</p>

<p>Po všech těchto formalitách je možné na rektorátu podepsat s panem Požárem finanční dohodu. Stipendium přijde na účet jednorázově cca. v polovině března.</p>


<h3>Očkování</h3>


<p>Očkování je jednoznačně nutné a čím více, tím lépe. Hygientické podmínky nejsou v Rusku zrovna nejlepší a samotná města připomínají na většině míst smetiště, zvlášť poté co odtaje sníh. Popelnice nemají víka, takže stačí trochu větru a všechno leží na ulici, lidé háží věci spíše na zem, než do odpadkových košů, a to i v případě, že stojí metr od něj. Po odtání sněhu se občas objeví pejsci a kočičky, kteří se už ovšem ze zimního spánku v závěji bohužel neprobudí. Člověk bývá leckdy překvapen, když na něj nějaké ležící zvířátko mrkne. Mezi Čechy na toto téma vznikla hláška <em>A co, odhnije.</em></p>

<p>Nechat se očkovat je možné na <a href="http://www.ockovacicentrum.cz/">http://www.ockovacicentrum.cz/</a>. Klíšťová encefalitida je pro Krasnoyarsk naprosto nezbytná (tři dávky, po druhé vzniká imunita, dává se měsíc po první, třetí vlétě). Pro Rusko je dále určitě vhodná žloutenka a někde jsem se dočetl i o břišním tyfu a vzteklině. Kdybych věděl do čeho jedu, tak nelituji peněz a očkuji se na všechno. Zkontrolujte si také očkování proti tetanu.</p>


<h3>Pojištění</h3>


<p>Pojištění na celou dobu pobytu stojí cca. 6000 Kč. Pozor, pojišťovny mohou mít různé tarify a je nutné se ptát na <strong>asijskou</strong> část ruska, která na rozdíl od <strong>evropské</strong> spadá do tarifu pro celý svět. Je dobré vědět, na co se pojištění vztahuje, rozhodně by tam mělo patřit stomatologické ošetření. Ačkoli jsem před odjezdem svého zubaře navštívil, hned v prvním měsíci pobytu se mi odlomil kousek zubu, což stojí cca. 2000 rublů (~ 1300 Kč). Není třeba se takových věcí bát, zubaři jsou v Rusku na mnohem vyšší úrovni, než u nás - respektive, ten, kde jsem byl já.</p>

<p>Alternativou může být čtvrtletní pojištiní přes banku k platební kartě a na zbytek pobytu si vyřídit standardní pojištění od pojišťovny.</p>


<h3>Co s sebou</h3>

<h4>Finance</h4>

<p>Koruny, kromě směnáren na letišti v Moskvě (kurz 1:1!), v Rusku nevyměníte, takže netřeba vozit. Zahraničním studentům se při cestě do česka doporučuje do začátku 6000 Kč, šel jsem stejnou cestou. V Praze není problém najít směnárnu, kde se dají vyměnit koruny za rubly, stačí chvilka googlování. Eura netřeba.</p>

<p>Co vím, v mBank jsou výběry z bankomatu v zahraničí zadarmo, jinde se platí fixní poplatek v řádu stokorun. Je dobré mít kartu Visa Classic, protože například s Visa Electron letenka zaplatit nejde.</p>


<h4>Fotografie</h4>

<p>Určitě je třeba spousta fotografií, 10 je spíš minimum. Na ČVUTu je jedna univerzální karta na všechno, v Krasnoyarsku je potřeba na každou blbost zvláštní průkaz.</p>



