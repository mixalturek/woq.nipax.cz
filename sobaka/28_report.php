<h3>Ubytování</h3>

<h4>Общежитие № 1 - Черемушки</h4>

<p>Před odjezdem se ptejte, kde vás chtějí ubytovat. Pokud sem, odmítněte to z důvodu obav o své zdraví, Čeremušky jsou nejhorší rajon města. Když jsem se po příjezdu ptal známých, kde je co v Krasnojarsku k vidění, jako první ukázali na mapu se slovy "tady jsou Čeremušky, sem nechoď ani ve dne". Tato kolej je navíc vzdálená od univerzity půl hodiny autobusem a do centra další půl hodiny. Ubytování je tam prý díky rekonstrukci pro zahraniční studenty hodně dobré (ruská logika), ale sem rozhodně ne.</p>


<h4>Общежитие № 2 - Инструментальная ул., 1</h4>

<p>Obrovskou výhodou této koleje je, že se nachází blízko univerzity. Korpus Д (jazyky, humanitní vědy, ...) je sousední budova, korpusy А (hlavní korpus, mezinárodní oddělení, internetová učebna, jídelna, ...), П (asi jenom administrativa) a Н (počítačové předměty, ruština, ...) jsou přes cestu, korpus Е (asi humanitní vědy) pět minut daleko, bazén a hala deset minut, knihovna minutu. Centrum města (Divadlo opery a baletu) je cca. 20 minut autobusem daleko.</p>

<p>Ubytování je <em>strahovského</em> typu, pokoj je o něco větší než na Strahově, bydlím sám. Rusové bývají po dvou i po třech, takže pokud přijede víc zahraničních studentů najednou, je pravěpodobné, že budou spolu. Zařízení pokoje je kompletně nové, těsně před mým příjezdem proběhl remont. Je tu skříň, dvoupatrová postel, na které se nedá sedět :-(, stůl, dvě židle bez opěradel :-( a noční stolek. Dva dny po příjezdu byla dodána lednička a rychlovarná konvice, určitě se na ně zeptejte, pokud nedorazí.</p>

<p>Koupelna a záchod jsou společné, první týden byl vyloženě otřesný, poté si zvyknete a je to už jenom nepříjemné. Po dvou měsících považuji za luxus, když si můžu na záchodě sednout ;-) Dva týdny po mém příjezdu proběhl remont i této části ubytovacího zařízení, spočíval však pouze v tom, že byly natřeny stěny. Viděl jsem to na vlastní oči, nicméně ani při bližším zkoumání jsem následně nedokázal rozlišit, které stěny nebo jejich části byly natřeny.</p>

<p>Těsně před mým odjezdem začal další z remontů, tentokrát kompletní, vyměnily se dlaždičky, umyvadla, přišly klasické záchody. Ze začátku jsem za to byl hodně rád, poté jsem si říkal, jestli to předtím nebylo lepší. Mnozí z Rusů jsou, s prominutím, prasata a bezkontaktní záchody, ačkoli hodně nepohodlné, mají něco do sebe.</p>

<p>Sprchy jsou taktéž společné, nachází se v přízemí, tři mužské a tři ženské (ano, pro celou kolej). Určitě si hned na začátku pobytu kupte umělohmotné pantofle, budou se hodit. Do sprchy se může od osmi ráno do deseti večer, v pondělí je sanitární den a jsou zavřené (prý aby vyschly). Po zkušenostech ze záchodů jsem měl velký strach, ale není to tak strašné. Někdy si říkám, že asi proto, že jsem čekal něco mnohem, mnohem horšího.</p>

<p>Dále se zde nachází posilovna a občas je slyšet stolní tenis. Pračka stojí 40 rublů/vyprání. Platí se pětirublovými mincemi, které se vhazují přímo do pračky jako do automatu. Pokud jich budete mít akutní nedostatek, poptejte se u konduktora v autobuse, rád se jich zbaví ;-)</p>

<p>Za ubytování se platí cca. 600 rublů za měsíc.</p>

<p>Internet je bezlimitní, 200 rub./měsíc, občas nefunguje, ale to je tady normální úplně všude. Ze začátku jsem měl pouze wifi, což bylo utrpení, protože přestalo fungovat vždy, když, když si chtěl Володя ohřát vodu na čaj. Kabel by prý bylo složité udělat. Poté, co si Володя vzal zařízení domů, mi byl ovšem kabel přislíben, ale vždy to ztroskotalo na nějaké maličkosti. Jeden týden мальчик, který natahuje kabely odjel domů, po víkendu se sice vrátil, ale druhý мальчик, který mu vždy pomáhá, odjel domů apod. Po dvaceti dnech jsem zanechal téměř každodenních dotazů a smířil se se skutečností, že internet prostě nebude. Dvacátý první den večer přišli a za deset minut natáhli kabel. Zlí jazykové tvrdí, že slovo минуточка může v rusku zahrnovat dobu od jedné minuty až do několika let.</p>


<h4>Общежитие № 3 - Волгоградская, 35</h4>

<p>Pokud vás ubytují sem, pravděpodobně to bude do bytů pro hosty, ve kterých je pokoj, kuchyň, koupelna i záchod. Je tu i menza, kde vaří mnohem lépe než v hlavním korpusu. Vzdálenost do školy něco kolem deseti minut pěšky. Doporučuji.</p>


<h3>Registrace na univerzitě, úřadech - poplatky, postupy, kontaky</h3>

<p>Registrace na univerzitě (mezinárodní oddělení) byla naprosto bez problémů, všichni jsou přátelští a tykají od první minuty. Je potřeba spousta fotek pasového formátu, deset je absolutní minimum, na každou věc je potřeba zvláštní průkazka. Před odjezdem je dobré si udělat kopii pasu a víza, na jeden den vám ho vezmou, aby mohli zaregistrovat pobyt. Ono obecně mít od důležitých dokumentů v kufru kopie nemusí být špatný nápad.</p>

<p>Na prodloužení víz jsou potřeba 4 matné fotografie, asi nejjednodušší je nechat si je udělat tady, a 400 rublů. Po deseti dnech jsou hotová.</p>


<h3>Zápis předmětů</h3>

<p>Zápis předmětů je v podstatě bezproblémový, akorát chvíli trvá. Hned na začátku jsem požádal o předměty ze studijního plánu, které jsem si vybral už v česku. Tehdy jsem je vybíral naslepo, pouze podle názvu, a proto se v nich vyskytla polovina pro bakaláře. Není to problém, ale doporučuji si s tím více pohrát. Mě to bylo jedno, protože všechny povinné předměty už mám v Česku hotové a do <em>celofakultní nabídky</em> se dá použít cokoli.</p>

<p>Doporučuji si vybrat i jeden, dva předměty z humanitární fakulty. Potřeboval jsem jeden ekonomicko-manažerský předmět, a proto jsem si vyloženě s nechutí zapsal Экономику природопользования. Nelitoval jsem, více než polovina studentů jsou slečny ;-)</p>

<p>První předměty se začínají objevovat až po prvním týdnu, protože se musí vše z nějakého důvodu domlouvat přímo s učiteli. Nechápu proč, protože je potom vše stejně v ruštině s ruskými studenty. Učit se začnete asi tak po dvou týdnech po příjezdu.</p>


<h2>Studium</h2>

<h3>Úroveň a forma studia</h3>

<p>Výuka probíhá v ruštině s ruskými studenty, žádná speciální výuka v angličtině není. Rusové se učí po skupinách, které jsou od začátku do konce studia stejné, takže se všichni mezi sebou znají a to i včetně učitelů. Styl výuky, věk studentů, psaní do sešitů A5, písemky v semestru, opisování, taháky atd. způsobují, že si člověk připadá, jako na střední škole. Neříkám, že to je tento styl výuky něco špatného, spíš nezvykle jiný, s výhodami i nevýhodami. Onehdá jsem přeposílal papírek vytržený ze sešitu s nápisem "Ahoj, jak je?", ale tohle byl už fakt extrém :-D</p>

<p>Přednášet se rusky řekne читать лекцию, na hodinách ruštiny jsme se tomu hodně smáli. Bohužel se opravdu v naprosté většině předmětů jedná o čtení u přednášejícího a psaní u studentů.</p>

<p>Zahraniční studenti jsou neobvyklí, a proto způsobují zájem nejen u studentů ale i učitelů. Je hodně znát, že jste <em>něco jiného</em> než ostatní. Cvičení i přednášky jsou povinné, ale při prezenci své jméno v seznamu nenajdete.</p>

<p>Pokud je vám tematika předmětu blízká a sezení na hodinách vám připadá jako ztráta času, zkuste se s vyučujícími domluvit, co všechno je potřeba znát nebo udělat, aby vám zapsali zápočet. Mnoho z nich vám zadá nějaký individuální projekt, domluví se s vámi na konzultacích a ukázkách dosavadního průběhu práce a pokud všechno uděláte, máte zápočet a v ojedinělých případech i kompletně zakončený předmět.</p>

<p>Určitě se na začátku výuky zeptejte, co všechno je třeba zvládnout k zakončení předmětu. Občas učitelé neví ani jestli bude předmět zakončen zkouškou nebo bez zkoušky (prostě Rusko), ale často se dozvíte zajímavé a obecně prospěšné informace, které vám později ušetří spoustu času.</p>


<h3>Vybavení - kampusy, menzy</h3>

<p>Ve vybavení není nic speciálního, ale také nic nechybí. Píše se na tabule, prezentace na počítačích ještě nedorazily, projektory jsou spíše neobvyklé.</p>

<p>Je znát výrazný nedostatek veřejně přístupných počítačových učeben. Ruští studenti mají limit cca. dvě hodiny za týden, kdy mají přístup k internetu. U zahraničních studentů tento limit není, nicméně dívat se do starých CRT monitorů s mizerným zaostřením oči dlouho nezvládají.</p>

<p>Žádné wifi, ale občas bývá v kavárnách.</p>

<p>Ceny v menze jsou výrazně nižší, než kdekoli jinde ve městě, odpovídá tomu však i kvalita jídla a prostředí. Všechno má kašovitou konzistenci, včetně mletého masa. Díky tomu není nutný nůž, takže se jí pouze hliníkovou lžičkou a vidličkou. Mají skvělé polévky, převážně zeleninové (борщ, щи, ...), ty jsem opravdu mohl. Platí se vždy hotově, průměrná cena kolem třiceti rublů, záleží co a kolik si vezmete. Pokud se najíte bez masa, dostanete se bez problémů i k deseti rublům. Na druhou stranu, vegetariánství je na Sibiři neznámý pojem.</p>


